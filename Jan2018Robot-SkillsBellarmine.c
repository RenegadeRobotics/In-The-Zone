// finding the average between right and left Encoders in a task
int encAve;
task encAvgTask(){
	while(true){
		encAve = (SensorValue[rtBack] + SensorValue[ltBack]) / 2;
	}
}

// drive, setting motors in a function
void driveFunc(int rightPower, int leftPower){
	SetMotor (FR, rightPower);
	SetMotor (FL, leftPower);
	SetMotor (BR, rightPower);
	SetMotor (BL, leftPower);
}

void encDriving(int power, int encClicks){
	SensorValue[rtBack] = 0;
	SensorValue[ltBack] = 0;

	driveFunc(power,power);


	while(encAve < encClicks){

	}
	driveFunc(0,0);
}


// Setting MG Lift joystick buttons
// into task so that it will allow chassis to run at same time
int MGliftPower = 0;
task MGLiftTask(){
	while(true){
		// set up & down buttons
		MGliftPower = (vexRT[Btn6DXmtr2] - vexRT[Btn6UXmtr2]) * 90;
		SetMotor(MGLift, MGliftPower);

		// MG lift - move-to-bottom button
		if(vexRT[Btn8DXmtr2] == 1){
			if(SensorValue[MG] > 190){
				while(SensorValue[MG] > 600){
					SetMotor(MGLift, 90);
					wait1Msec(20);
				}
				SetMotor(MGLift, 0);
			}

			else{
				while(SensorValue[MG] < 250){
					SetMotor(MGLift, -50);
					wait1Msec(20);
				}
				SetMotor(MGLift, 0);



			}

		}
	}
}


