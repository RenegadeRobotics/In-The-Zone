task autonomous()
{
	startTask(encAvgTask);

	//move backward to distance self from cone and stop
	driveFunc(-90, -90);
	wait1Msec(150);
	driveFunc(0, 0);
	wait1Msec(20);

	//pick up cone(arm down, and roll intake)
	clearTimer(T1);
	int killTimer = 2500;
	while ((SensorValue[Arm] > -264) && (time1[T1] < killTimer)){
		SetMotor(ConeArm, -90);
		wait1Msec(20);
	}
	SetMotor (ConeArm, 0);

	SetMotor (Roller, 127);
	wait1Msec(600);
	SetMotor (Roller, 20);


	//arm up (straight)
	while (SensorValue[Arm] < -190){
		SetMotor(ConeArm, 90);
		wait1Msec(20);
	}
	SetMotor(ConeArm, 0);

	// move MG to lowest level
	while (SensorValue[MG] < 250){
		SetMotor(MGLift, -50);
		wait1Msec(20);
	}
	SetMotor(MGLift, 0);
	wait1Msec(20);

	//drive backward to get mobile goal
	encDriving(-60,1550);

	//move MG picker upper up
	while (SensorValue[MG] < 800){
		SetMotor(MGLift, -90);
		wait1Msec(20);
	}
	SetMotor(MGLift,0);
	wait1Msec(20);

	// place cone on mobile goal
	clearTimer(T1);
	killTimer = 1500;
	while ((SensorValue[Arm] < -29) && (time1[T1] < killTimer)){
		SetMotor(ConeArm, 25);
		wait1Msec(20);

	}
	SetMotor(ConeArm, 0);
	SetMotor(Roller, 0);
	wait1Msec(20);

	// Turn after picking up mobile goal to face zone
	//180 degrees
	driveFunc(-80, 80);
	wait1Msec(1200);

	driveFunc(0, 0);
	wait1Msec(50);

	//hold MGLift up
	setMotor(MGLift, -60);
	wait1Msec(20);

	/*DRIVE FORWARD
	encDriving(-127,400); */

	//drive forward
	driveFunc(-80, -80);
	wait1Msec(725);

	driveFunc(0, 0);
	wait1Msec(50);

	//turn zigzag #1
	driveFunc(-80, 80);
	wait1Msec(750);

	driveFunc(0, 0);
	wait1Msec(50);

	//straight #2
	encDriving(-127,800);

	// MGLift, lift up higher
	SetMotor(MGLift, -90);

	//turn zig zag #2
	driveFunc(80, -80);
	wait1Msec(750);

	driveFunc(0, 0);
	wait1Msec(50);

	//drive to 20 point zone
	encDriving(-127,2000);

	setMotor(MGLift, 0);
	wait1Msec(50);

	//turn off MGLift
	setMotor(MGLift, 0);
	wait1Msec(50);

	//rolly outake lift up arm
	SetMotor(Roller, -127);
	wait1Msec(200);
	while (SensorValue[Arm] > -100){
		SetMotor(ConeArm, -127);
		wait1Msec(20);
	}
	SetMotor (ConeArm, 0);
	SetMotor (Roller, 0);
	wait1Msec(50);

	//MGpusher to push cone off MG
	SetMotor (MGpusher, 127);
	wait1Msec(1500);

	SetMotor (MGpusher, 0);

	// drive forward to ensure cone and MG go over
	driveFunc(-127, -127);
	wait1Msec(500);

	// drive away
	driveFunc(127, 127);
	wait1Msec(500);

	driveFunc(0, 0);

	//drive diagonally
	driveFunc(127,60);
	wait1Msec(2000);

	driveFunc(127,127);
	wait1Msec(2000);

	driveFunc(0,0);

	stopTask(encAvgTask);
}




